    private void armarCombo() {
        this.jComboBoxDietasTabla.removeAll();
        this.jComboBoxDietasTabla.addItem("Seleccione un Estado");
        this.jComboBoxDietasTabla.addItem(Estado.TODOS);
        this.jComboBoxDietasTabla.addItem(Estado.ACTIVO);
        this.jComboBoxDietasTabla.addItem(Estado.INACTIVOS);
        this.jComboBoxDietasTabla.setSelectedIndex(0);
    }

    private void initTable() {
        this.model.addColumn("Codigo");
        this.model.addColumn("Nombre");
        this.model.addColumn("Paciente");
        this.model.addColumn("Fecha de inicio");
        this.model.addColumn("Fecha de fin");
        this.model.addColumn("peso inicial");
        this.model.addColumn("peso final");
        
        this.jTableDietas.setModel(model);
        
        JTableHeader tableHeader = jTableDietas.getTableHeader();
        tableHeader.setDefaultRenderer(new DefaultTableCellRenderer() {
            @Override
            public Component getTableCellRendererComponent(JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column) {
                Component c = super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column);
                c.setPreferredSize(new Dimension(c.getWidth(), 40)); 
                c.setBackground(Color.GRAY); 
                c.setForeground(Color.WHITE);
                return c;
            }
        });
        
        tableHeader.getColumnModel().getColumn(0).setPreferredWidth(30);
      
    }

